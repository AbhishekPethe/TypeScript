
//  Array notation  //

const arr1: number[] = [12, 3, 4, 5]

const arr2: Array<string> = ['a', 'v', 'c']

const arr3: any[] = [13, 4, 5, 'a']

console.log(arr1);
console.log(arr2);
console.log(arr3);


//  Multidimensional Array  //

const arr4: number[][] = [[1, 2, 3, 4]]
const arr5: number[][][] = [[[1,2,3,4,5]]]
console.log(arr4);
console.log(arr5);

